LGap = 5

def Levenstein_score(u,v):
    m,n = len(u),len(v)
    T = [[float('-inf')]*(n+1) for _ in range(m+1)]
    T[0][0] = 0
    for i in range(m+1):
        for j in range(n+1):
            if i>0:
                T[i][j] = max(T[i][j],T[i-1][j]-LGap)
            if j>0:
                T[i][j] = max(T[i][j],T[i][j-1]-LGap)
            if i>0 and j>0:
                T[i][j] = max(T[i][j],T[i-1][j-1]+B[Num[u[i-1]]][Num[v[j-1]]])
    return T[m][n]

